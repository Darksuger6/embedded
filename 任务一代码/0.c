#include <REGX52.H>



void Timer0_Init()		//100微秒@11.0592MHz
{
	TMOD = 0x11;	//设置定时器模式
	TL0 = 0xA4;		//设置定时初始值
	TH0 = 0xFF;		//设置定时初始值
	TF0 = 0;			//清除TF0标志
	TR0 = 1;			//定时器0开始计时
	ET0 = 1;      //定时器0的中端允许开关
	EA  = 1;      //定时器总开关
	PT0 = 1;      //设置定时器为高低控制优先级，1为高优先级，0为低优先级
}


